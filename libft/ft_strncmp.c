#include "libft.h"

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	if (!n)
		return (0);
	while (n--)
	{
		if (*str1 != *str2)
		{
			return (*str1 - *str2);
		}
		if (!*str1 && !*str2)
			return (0);
		str1++;
		str2++;
	}
	return (0);
}
